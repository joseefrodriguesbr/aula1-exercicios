// =======================================================
// QUESTÃO 2: Conversão de Celsius para Fahrenheit
// Fórmula: F = C * 9/5 + 32
// =======================================================

let temperaturaCelsius: Double = 25.0 
let temperaturaFahrenheit: Double = temperaturaCelsius * (9.0 / 5.0) + 32.0

print("Temperatura em Celsius: \(temperaturaCelsius) ºC")
print("Temperatura em Fahrenheit: \(temperaturaFahrenheit) ºF")
print("--------------------------------")